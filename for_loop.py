#program for for-loop
print("values with only 1 parameter:")
for i in range(5):
    print(i)
print("values with 2 parameter:")
for i in range(1,5):
    print(i)
print("values with 3 parameter:")
for i in range(1,10,2):
    print(i)
print("values with  decrementing parameter:")
for i in range(10,1,-2):
    print(i)

